# Kernel SVM
'''
When there are data points which are not linearly separable, the Kernel SVM will be used.
Kernel SVM does divide the classification linearly, but taking in consideration of errors,
and making sure the model is properly optimized even though there are errors in seperation.
'''
'''
For SVM is to sperate the data linearly, it can be done by converting the data into higher 
dimension.
Through that, it can successfully divides the data linearly.
After that, it will transform the data back to its original form, which would make a
linear seperable to a non linear seperable.
However, it is not advisable to use this technique for prediction, as it would bring more
complex programming & process to solve by transforming data back & forth from one dimension
to another.
'''

'''
Kernel SVM brings in consideration the data which is non linearly distributed.
It works on Gausian Kernel principle
It assigns the random kernel to the data points, and from that data points, uses the
Gausian Kernel principle to classify the data points in different categories.
'''

'''
Type of Kernel
1. Gausian RBF Kernel
2. Sigmoid Kernel 
3. Polynomial Kernel 
'''


# Importing the libraries
import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

# Importing the dataset
dataset = pd.read_csv('Social_Network_Ads.csv')
X = dataset.iloc[:, [2, 3]].values
y = dataset.iloc[:, -1].values

# Splitting the dataset into the Training set and Test set
from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.25, random_state = 0)

# Feature Scaling
from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)

# Training the Kernel SVM model on the Training set
from sklearn.svm import SVC
classifier = SVC(kernel = 'rbf', random_state = 0)
classifier.fit(X_train, y_train)

# Predicting the Test set results
y_pred = classifier.predict(X_test)

# Accuracy of the model
classifier.score(X_test, y_test)

# Making the Confusion Matrix
from sklearn.metrics import confusion_matrix
cm = confusion_matrix(y_test, y_pred)
print(cm)

# Visualising the Training set results
from matplotlib.colors import ListedColormap
X_set, y_set = X_train, y_train
X1, X2 = np.meshgrid(np.arange(start = X_set[:, 0].min() - 1, stop = X_set[:, 0].max() + 1, step = 0.01),
                     np.arange(start = X_set[:, 1].min() - 1, stop = X_set[:, 1].max() + 1, step = 0.01))
plt.contourf(X1, X2, classifier.predict(np.array([X1.ravel(), X2.ravel()]).T).reshape(X1.shape),
             alpha = 0.75, cmap = ListedColormap(('red', 'green')))
plt.xlim(X1.min(), X1.max())
plt.ylim(X2.min(), X2.max())
for i, j in enumerate(np.unique(y_set)):
    plt.scatter(X_set[y_set == j, 0], X_set[y_set == j, 1],
                c = ListedColormap(('red', 'green'))(i), label = j)
plt.title('Kernel SVM (Training set)')
plt.xlabel('Age')
plt.ylabel('Estimated Salary')
plt.legend()
plt.show()

